
import 'dart:ui';

class Friend {
  final String name;
  final String email;
  final String imgUrl;
  final String descripcion;
  final String number;
  final bool online;
  final String scheduleUrl;
  const Friend({
    required this.name,
    required this.email,
    required this.imgUrl,
    required this.descripcion,
    required this.number,
    required this.online,
    required this.scheduleUrl
  });
}